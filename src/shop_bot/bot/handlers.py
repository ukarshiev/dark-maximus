def _resolve_instructions_dir() -> Path:
    candidates = [
        Path("/app/project") / "instructions",
        Path(__file__).resolve().parents[3] / "instructions",
        Path.cwd() / "instructions",
    ]
    for p in candidates:
        try:
            if p.exists():
                return p
        except Exception:
            continue
    return candidates[0]

